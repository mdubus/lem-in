#include "includes/libft.h"

void	ft_free_tab_int(int ***tab)
{
	int	i;

	i = 0;
	while ((*tab)[i] != NULL)
		ft_memdel((void**)&(*tab)[i++]);
	ft_memdel((void**)tab);
}
